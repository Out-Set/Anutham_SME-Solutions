# Initializing list1
list1 = [1, 2, 3]

# assigning list1 to list2
list2 = list1

# Creating a 2-dimensional list(list3), by putting the list1 and list2
list3 = [list1, list2]

print("list3: ",list3)

# indexing starts from zero

# value at first[0] row and second[1] column in the list3
val1 = list3[0][1]
print("val1: ",val1)

# value at second[1] row and first[0] column in the list3
val2 = list3[1][0]
